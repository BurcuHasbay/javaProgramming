package day06_PrimitiveTypeCastings;

public class Birthday {

    public static void main(String[] args) {

        String name = "Osman";
        int birthday = 02;
        String birthMonth = "September";
        int birthYear = 1993;

        System.out.println(name + " was born on " + birthMonth + "/" +
                birthday + "/" + birthYear + "." );


        System.out.println("---------------------------------------");

        String favoriteBook = "The rich Dad, The poor Dad";

        System.out.println("My favorite book is " + "'" +
                favoriteBook + "'" + ".");


    }

}
