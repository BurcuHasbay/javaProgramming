package day08_IfStatements;

public class SingleIfStatementIntro {

    public static void main(String[] args) {

        int n = 200;

        if (n > 0) {
            System.out.println(n + " is positive.");
        }

        if (n < 0) {
            System.out.println(n + " is negative");
        }

        if (n == 0) System.out.println(n + " is zero.");


    }
}